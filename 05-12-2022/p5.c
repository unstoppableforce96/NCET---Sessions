// Assignment Operators
#include <stdio.h>
int main()
{
	int a;
	a = 10;
	printf("Value of a = %d", a);
	a += 6; // a = 16
	printf("\nvalue of a = %d", a);
	a -= 8; // a= 8
	printf("\nvalue of a = %d", a);
	a *= 4; // a = 32
	printf("\nvalue of a = %d", a);
	a /= 16; // a = 2
	printf("\nvalue of a = %d", a);
	a %= 2; // a = 0
	printf("\nvalue of a = %d", a);
}